defmodule GithubStats.Github.ClientTest do
  use ExUnit.Case, async: true

  alias GithubStats.Github.Client

  alias Plug.Conn

  describe "get_repos/1" do
    setup do
      bypass = Bypass.open()

      {:ok, bypass: bypass}
    end

    test "when there is a valid username, returns the repos", %{bypass: bypass} do
      url = endpoint_url(bypass.port)

      username = "grhamm-pabst"

      Bypass.expect(bypass, "GET", "#{username}/repos", fn conn ->
        conn
        |> Conn.put_resp_header("content-type", "application/json")
        |> Conn.resp(200, resp_body())
      end)

      response = Client.get_repos(url, username)

      expected_response = {:ok, body()}

      assert response == expected_response
    end

    test "when there is an invalid username, returns an error", %{bypass: bypass} do
      url = endpoint_url(bypass.port)

      username = "grhamma-pabst"

      error_body = ~s(
        {
          "message": "Not Found",
          "documentation_url": "https://docs.github.com/rest"
        }
      )

      Bypass.expect(bypass, "GET", "#{username}/repos", fn conn ->
        conn
        |> Conn.put_resp_header("content-type", "application/json")
        |> Conn.resp(404, error_body)
      end)

      response = Client.get_repos(url, username)

      expected_response = {
        :error,
        %GithubStats.Error{result: "User not found!", status: :not_found}
      }

      assert response == expected_response
    end

    test "when there is a generic error, returns an error", %{bypass: bypass} do
      username = "grhamma-pabst"

      url = endpoint_url(bypass.port)

      Bypass.down(bypass)

      response = Client.get_repos(url, username)

      expected_response =
        {:error, %GithubStats.Error{result: :econnrefused, status: :bad_request}}

      assert response == expected_response
    end
  end

